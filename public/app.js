    ctx.lineTo(20, 160);
    ctx.lineTo(20, 100);
    ctx.moveTo(180, 100);
    ctx.lineTo(180, 160);
    ctx.lineTo(240, 160);
    ctx.lineTo(240, 220);
    ctx.lineTo(360, 220);
